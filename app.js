// Kerjakan kode kalian di bawah ini
for (let x = 1; x <= 20 ; x++) {
    console.log(x + ' - ' + 'Coding asyik banget, Cuy !');
}

for (let y = 20; y >= 1 ; y--) {
    console.log(y + ' - ' + 'Coding asyik banget, Cuy !');
}

let w = 2;
while (w <= 20) {
    console.log(w + ' - ' + 'Coding asyik banget, Cuy !');
    w += 2;
}

let v = 20;
while (v >= 2) {
    console.log(v + ' - ' + 'Coding itu harus latihan setiap hari !');
    v -= 2;
}