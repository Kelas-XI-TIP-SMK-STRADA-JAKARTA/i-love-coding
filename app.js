for (no = 1 ; no <= 20; no++) {
    console.log (no + " " + '-'+ " " +'Coding Asik Banget Cuy !');
}
console.log("---------------------------------------------")
console.log("----- PERULANGAN FOR KEDUA -----");

for (no = 20 ; no >= 1; no--) {
    console.log (no + " " + '-'+ " " +'Coding itu harus latihan setiap hari!');
}
console.log("---------------------------------------------")
console.log("----- PERULANGAN WHILE PERTAMA -----");

var no = 2;
while (no <= 20) {
    console.log(no + " " + "-" + " " + 'Coding Asik Banget Coy !');
    no = no + 2; 
}
console.log("--------------------------------------------")
console.log ("----- PERULANGAN WHILE KEDUA -----");

var no = 20;
while (no >= 2) {
    console.log(no + " " + "-" + " " + 'Coding itu harus latihan setiap hari!');
    no = no - 2;
}
console.log("--------------------------------------------")